//DELETE profile experience - authenticated
router.delete(
  "/experience/:exp_id",
  passport.authenticate("jwt", { session: false }),
  (req, res) => {
    Profile.findOne({ user: req.user.id })
      .then(profile => {
        //Get the index
        const removeIndex = profile.experience
          .map(item => item.id) //map over all id in experience array
          .indexOf(req.params.exp_id); //get the value of selected id
        profile.experience.splice(removeIndex, 1); //splice out selected id for removal
        profile.save().then(profile => res.json(profile)); //save profile with removed experience
      })
      .catch(err => res.status(404).json(err));
  }
);

